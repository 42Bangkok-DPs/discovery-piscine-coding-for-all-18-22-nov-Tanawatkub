#!/usr/bin/env python3

frist_number = float(input("Give me the first number: "))
second_number = float(input("Give me the second number: "))
print("Thank you!")

print(f"{frist_number} + {second_number} = {frist_number + second_number}")
print(f"{frist_number} - {second_number} = {frist_number - second_number}")
print(f"{frist_number} / {second_number} = {frist_number / second_number}")
print(f"{frist_number} * {second_number} = {frist_number * second_number}")